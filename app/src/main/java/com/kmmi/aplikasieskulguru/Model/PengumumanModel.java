package com.kmmi.aplikasieskulguru.Model;

public class PengumumanModel {

    private int idpengumuman;
    private String judulPengumuman;
    private String isiPengumuman;
    private String status;
    private String fotoPengumuman;
    private int ideskul;

    public int getIdpengumuman() {
        return idpengumuman;
    }

    public void setIdpengumuman(int idpengumuman) {
        this.idpengumuman = idpengumuman;
    }

    public String getJudulPengumuman() {
        return judulPengumuman;
    }

    public void setJudulPengumuman(String judulPengumuman) {
        this.judulPengumuman = judulPengumuman;
    }

    public String getIsiPengumuman() {
        return isiPengumuman;
    }

    public void setIsiPengumuman(String isiPengumuman) {
        this.isiPengumuman = isiPengumuman;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getFotoPengumuman() {
        return fotoPengumuman;
    }

    public void setFotoPengumuman(String fotoPengumuman) {
        this.fotoPengumuman = fotoPengumuman;
    }

    public int getIdeskul() {
        return ideskul;
    }

    public void setIdeskul(int ideskul) {
        this.ideskul = ideskul;
    }
}
